    private void btnUserActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUserActionActionPerformed
        String firstName = tfUsername.getText();
        String lastName = String.valueOf(tfPassword.getPassword());
        String role = cbRole.getSelectedItem().toString();
        User user = new User(firstName, lastName, role);
        if (firstName.trim().isEmpty() || lastName.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field harus diisi!");
            return;
        }
        try {
            if (isEdit) {
                int index = listTable.getSelectedRow();
                repository.updateUser(index, user);
                tableModel.fireTableDataChanged();
            } else {
                repository.register(firstName, lastName, role);
                tableModel.fireTableRowsInserted(0, users.size() - 1);
                clearTextFields();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnUserActionActionPerformed

    private void btnDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserActionPerformed
        int index = listTable.getSelectedRow();
        User user = users.get(index);
        if (user.getUsername().equals(currentUser.getUsername())) {
            JOptionPane.showMessageDialog(this, "Tidak bisa menghapus akun sendiri!");
        } else {
            repository.deleteUser(index);
            tableModel.fireTableDataChanged();
            isEdit = false;
            setEditingMode();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteUserActionPerformed

