    public UserView(MainFrame mainFrame) {
        this.mainFrame = mainFrame;
        users = DatabaseService.getUsers();
        repository = InjectionContainer.authRepository;
        tableModel = new UserListTableModel(users);
        currentUser = DatabaseService.getCurrentUser();
        initComponents();

        cbRole.removeAllItems();
        cbRole.addItem(roles[0]);
        cbRole.addItem(roles[1]);

        listTable.setModel(tableModel);
        ListSelectionModel list = listTable.getSelectionModel();
        setEditingMode();
        // Listen to row selection
        list.addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting() && !isSelectionUserEvent) {
                isEdit = true;
                int selectedRow = listTable.getSelectedRow();
                if (selectedRow != -1) {
                    User data = tableModel.getUserAt(selectedRow);
                    tfUsername.setText(data.getUsername());
                    tfPassword.setText(data.getPassword());
                    cbRole.setSelectedIndex(data.getRole().equals(roles[0]) ? 0 : 1);
                    cbRole.setEnabled(!currentUser.getUsername().equals(data.getUsername()));
                }
                setEditingMode();
            }
        });
    }

    private void setEditingMode() {
        btnUserAction.setText(isEdit ? "Ubah" : "Tambah");
        btnCancelNasabah.setVisible(isEdit);
        btnDeleteUser.setVisible(isEdit);
        if (!isEdit) {
            clearTextFields();
            isSelectionUserEvent = true;
            listTable.clearSelection();
            isSelectionUserEvent = false;
            cbRole.setEnabled(true);
            cbRole.setSelectedIndex(0);
        }
    }

    private void clearTextFields() {
        tfUsername.setText("");
        tfPassword.setText("");
    }

